async function main() {
  console.log("ðŸŒ± Iniciando seeding de la base de datos...");

  try {
    // Crear la lista de palabras en inglÃ©s
    const englishWordList = await prisma.wordList.upsert({
      where: { name: "english_common" },
      update: {},
      create: {
        name: "english_common",
        lang: "en",
        description: "Palabras comunes en inglÃ©s para prÃ¡ctica de mecanografÃ­a",
      },
    });

    console.log(`âœ… Lista de palabras creada: ${englishWordList.name}`);

    // Limpiar palabras existentes de esta lista para evitar duplicados
    await prisma.word.deleteMany({
      where: { wordListId: englishWordList.id },
    });

    // Crear las palabras asociadas a esta lista
    const words = await prisma.word.createMany({
      data: commonEnglishWords.map((wordText) => ({
        text: wordText,
        wordListId: englishWordList.id,
      })),
      skipDuplicates: true,
    });

    console.log(
      `âœ… ${words.count} palabras agregadas a la lista "${englishWordList.name}"`
    );

    // Verificar que se crearon correctamente
    const wordCount = await prisma.word.count({
      where: { wordListId: englishWordList.id },
    });

    console.log(`âœ… Total de palabras en la base de datos: ${wordCount}`);
    console.log("ðŸŽ‰ Seeding completado exitosamente!");
  } catch (error) {
    console.error("âŒ Error durante el seeding:", error);
    throw error;
  }
}

